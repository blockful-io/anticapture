export const getMultilineChartDatasets = () => {
  if (multilineChart.length === 0) return {};

  const lastTimestamp = multilineChart[multilineChart.length - 1][0];

  const cutoffTimestamps = {
    "7d": lastTimestamp - 7 * 24 * 60 * 60 * 1000,
    "30d": lastTimestamp - 30 * 24 * 60 * 60 * 1000,
    "90d": lastTimestamp - 90 * 24 * 60 * 60 * 1000,
    "365d": lastTimestamp - 365 * 24 * 60 * 60 * 1000,
  };

  return {
    full: multilineChart,
    "7d": multilineChart.filter(
      ([timestamp]) => timestamp >= cutoffTimestamps["7d"],
    ),
    "30d": multilineChart.filter(
      ([timestamp]) => timestamp >= cutoffTimestamps["30d"],
    ),
    "90d": multilineChart.filter(
      ([timestamp]) => timestamp >= cutoffTimestamps["90d"],
    ),
    "365d": multilineChart.filter(
      ([timestamp]) => timestamp >= cutoffTimestamps["365d"],
    ),
  };
};

